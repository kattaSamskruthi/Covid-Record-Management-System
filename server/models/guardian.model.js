const mongoose = require("mongoose");
const Schema = mongoose.Schema;

const guardian = new Schema({
  name: {
    type: String,
    required: true,
  },
  location: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
});

const Guardian = mongoose.model("guardian", guardian);

module.exports = Guardian;
